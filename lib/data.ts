// Function to filter people by category
export const getPeopleByCategory = (category) => {
  if (category === "discover") {
    return peopleData
  }
  return peopleData.filter((person) => person.category.toLowerCase() === category.toLowerCase())
}

// Function to paginate data
export const paginateData = (data, page, itemsPerPage) => {
  const startIndex = (page - 1) * itemsPerPage
  const endIndex = startIndex + itemsPerPage
  return data.slice(startIndex, endIndex)
}

// Function to get person by ID
export const getPersonById = (id) => {
  return peopleData.find((person) => person.id === id)
}

